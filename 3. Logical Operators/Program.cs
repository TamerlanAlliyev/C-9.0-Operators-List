//  ! : Məntiqi DEYIL(Logical NOT)



// 3. Məntiqi Operatorlar (Logical Operators)
// && : Məntiqi VƏ (Logical AND)
bool a = true;
bool b = false;
bool andResult = a && b;  // Məntiqi VƏ əməliyyatı
Console.WriteLine("Məntiqi VƏ: " + andResult);  // Çıxış: False

// || : Məntiqi VƏ YA (Logical OR)
bool orResult = a || b;  // Məntiqi VƏ YA əməliyyatı
Console.WriteLine("Məntiqi VƏ YA: " + orResult);  // Çıxış: True

// ! : Məntiqi DEYIL (Logical NOT)
bool notResult = !a;  // Məntiqi DEYIL əməliyyatı
Console.WriteLine("Məntiqi DEYIL: " + notResult);  // Çıxış: False