// 4. Təyin etmə Operatorları (Assignment Operators)
// = : Təyin etmə (Assignment)
int a = 5;  // a dəyişəninə 5 dəyəri təyin edilir
Console.WriteLine("Təyin etmə: " + a);  // Çıxış: 5

// += : Toplama təyini (Addition assignment)
a += 3;  // a = a + 3; a dəyişəni indi 8 olacaq
Console.WriteLine("Toplama təyini: " + a);  // Çıxış: 8

// -= : Çıxarma təyini (Subtraction assignment)
a -= 2;  // a = a - 2; a dəyişəni indi 6 olacaq
Console.WriteLine("Çıxarma təyini: " + a);  // Çıxış: 6

// *= : Vurma təyini (Multiplication assignment)
a *= 4;  // a = a * 4; a dəyişəni indi 24 olacaq
Console.WriteLine("Vurma təyini: " + a);  // Çıxış: 24

// /= : Bölmə təyini (Division assignment)
a /= 3;  // a = a / 3; a dəyişəni indi 8 olacaq
Console.WriteLine("Bölmə təyini: " + a);  // Çıxış: 8

// %= : Modulus təyini (Modulus assignment)
a %= 5;  // a = a % 5; a dəyişəni indi 3 olacaq
Console.WriteLine("Modulus təyini: " + a);  // Çıxış: 3