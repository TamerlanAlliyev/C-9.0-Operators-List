//  1.Aritmetik Operatorlar(Arithmetic Operators)
//  + : Toplama(Addition)
//  - : Çıxarma(Subtraction)
//  * : Vurma(Multiplication)
//  / : Bölmə(Division)
//  % : Qalıq(Moduls)



// + : Toplama (Addition)
int a = 5;
int b = 3;
int sum = a + b;  // Toplama əməliyyatı
Console.WriteLine("Toplama: " + sum);  // Çıxış: 8

// - : Çıxarma (Subtraction)
int diff = a - b;  // Çıxarma əməliyyatı
Console.WriteLine("Çıxarma: " + diff);  // Çıxış: 2

// * : Vurma (Multiplication)
int product = a * b;  // Vurma əməliyyatı
Console.WriteLine("Vurma: " + product);  // Çıxış: 15

// / : Bölmə (Division)
int quotient = a / b;  // Bölmə əməliyyatı
Console.WriteLine("Bölmə: " + quotient);  // Çıxış: 1

// % : Modulus (Kalan)
int remainder = a % b;  // Modulus əməliyyatı
Console.WriteLine("Qalıq: " + remainder);  // Çıxış: 2